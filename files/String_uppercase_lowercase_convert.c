#include <string.h>


//--------------------------------------------------------------------------------
// ** Convert string from uppercase to lowercase
//--------------------------------------------------------------------------------
char *ToLower(char *s)
{
    for (int i = 0; s[i] != '\0'; i++)
    {
        if (s[i] >= 'A' && s[i] <= 'Z')
        {
            s[i] = s[i] + 32;
        }
    }
    return s;
};
//--------------------------------------------------------------------------------
// ** END: Convert string from uppercase to lowercase
//--------------------------------------------------------------------------------


//--------------------------------------------------------------------------------
// ** Convert string from lowercase to uppercase
//--------------------------------------------------------------------------------
char *ToUpper(char *s)
{
    for (int i = 0; s[i] != '\0'; i++)
    {
        if (s[i] >= 'a' && s[i] <= 'z')
        {
            s[i] = s[i] - 32;
        }
    }
    return s;
};
//--------------------------------------------------------------------------------
// ** END: Convert string from lowercase to uppercase
//--------------------------------------------------------------------------------
