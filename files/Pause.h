//---------------------------------------------------------------------------------------------
// ** Include files
//---------------------------------------------------------------------------------------------
#include <stdio.h>
#include <conio.h>


//---------------------------------------------------------------------------------------------
// ** Pause and wait for any keypress, shorthand. (console)
//---------------------------------------------------------------------------------------------
int pause()
{
    if (system(NULL))
    {
        return system("echo(&pause"); // "echo(&" = adds linebreak
    }
    else
    {
        printf("\nPress any key to continue . . .");
        return _getch();
    }
};
//---------------------------------------------------------------------------------------------


//---------------------------------------------------------------------------------------------
#endif // PAUSE_H
